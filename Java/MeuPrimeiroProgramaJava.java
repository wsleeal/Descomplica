
class MeuPrimeiroProgramaJava {
    public static void main(String entrada[]) {
        // Entrada de dados
        int n1 = Integer.parseInt(entrada[0]);
        int n2 = Integer.parseInt(entrada[1]);
        // Processamento
        int soma = n1 + n2;
        // Saida de resultados
        System.out.println(n1 + " + " + n2 + " = " + soma);
        System.exit(0);
    }
}